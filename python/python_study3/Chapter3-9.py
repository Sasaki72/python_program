# 【プログラミングを補足するコメントの書き方】
# Pythonでは「#」を使ってコメントを残す
# 
# 【「#」以降はコメント】
# 頭に「#」(半角)を付ける。
# これより、#以降の部分が無視される。
# 
# 
# 【COLUMN】《重要》
# エラーが出てうまく行かないときは次をチェックする
# 
# ・大文字と小文字の区別は正しいか
# ・全角文字で入力してないか
# ・行頭の空白の数は正しいか (指定されていない限り、行頭には空白を入れない)
# ・カッコの対応が間違っていないか
# ・シングルクォーテーション(')やダブルクォーテーション(")が間違っていないか
# ・文字コードは正しいか
# ・日本語を使っているとき、1行目や2行目に「coding=utf-8」か「coding:utf-8」を記述したか
# ・ファイル名に日本語を使っていないか
# 
# ※１つでも間違えるとエラーになる
# 
